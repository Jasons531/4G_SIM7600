//DMA发送完成中断回调函数
void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)  
{  
	if(huart->Instance==USART1)
	{
		__HAL_DMA_DISABLE(huart->hdmatx);  
		UART_RX_DATA1.USART_TX_Flag = USART_DMA_SENDOVER;  
	}
	else if(huart->Instance==USART2)
	{
		__HAL_DMA_DISABLE(huart->hdmatx);  
		UART_RX_DATA2.USART_TX_Flag = USART_DMA_SENDOVER;  
	}	
} 

//DMA发送函数 
void Usart1SendData_DMA(char *pdata, uint16_t Length)  
{  
	uint32_t time_over = HAL_GetTick();
    while(UART_RX_DATA1.USART_TX_Flag == USART_DMA_SENDING && HAL_GetTick()-time_over<10);  ///10ms超时保护机制
	
    HAL_StatusTypeDef Uart_Tx = HAL_UART_Transmit_DMA(&huart1, (uint8_t *)pdata, Length); 
	UART_RX_DATA1.USART_TX_Flag = USART_DMA_SENDING;  
}


//DMA发送函数 
void Usart2SendData_DMA(char *pdata, uint16_t Length)  
{  
	uint32_t time_over = HAL_GetTick();
  while(UART_RX_DATA2.USART_TX_Flag == USART_DMA_SENDING && HAL_GetTick()-time_over<10);  ///10ms超时保护机制
	
  HAL_StatusTypeDef Uart_Tx = HAL_UART_Transmit_DMA(&huart2,(uint8_t *)pdata, Length); 
	UART_RX_DATA2.USART_TX_Flag = USART_DMA_SENDING;  
}

